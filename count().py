# str.count is used to count the number of occurrences of a string in another

string = "To be or not to be"

be_count = string.count("be")
print(be_count)

to_count = string.count("to")
print(to_count)

o_count = string.count("o")
print(o_count)

o_range_count = string.count("o", 2, 12)
print(o_range_count)
